#include<stdio.h>
int main()
{
    int no1=5,no2=7;
    float float_no1=3.1,float_no2=2.3;
    char c_no1=2,c_no2=3;
    printf("addition operation no1 +no2 =%d\n",no1+no2);
    printf("addition operation float_no1 +float_no2 =%f\n",float_no1+float_no2);
    printf("addition operation c_no1 +c_no2 =%d\n",c_no1+c_no2);

    printf("subraction operation no1-no2=%d\n",no1-no2);
    printf("subraction operation float_no1-float_no2=%f\n",float_no1-float_no2);
    printf("subraction operation c_no1-c_no2=%d\n",c_no1-c_no2);
    
    printf("multiplication operation no1*no2=%d\n",no1*no2);
    printf("multiplication operation float_no1*float_no2=%f\n",float_no1*float_no2);
    printf("multiplication operation c_no1*c_no2=%c\n",c_no1*c_no2);

    printf("division operation no1/no2=%d\n",no1/no2);
    printf("division operation float_no1/float_no2=%f\n",float_no1/float_no2);
    printf("division operation c_no1/c_no2=%d\n",c_no1/c_no2);
    return 0;
}